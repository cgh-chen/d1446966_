# 第一段：印出 1 到 10
for i in range(1, 11):      # range(1, 11) 代表從 1 開始到 10（不包含 11）
    print(i, end="\t")      # end="\t" 表示輸出後不換行，用「Tab」分隔數字
print("\n", "--"*30)        # "\n" 是換行，"--"*30 代表重複 "--" 30 次作為分隔線


# 第二段：印出 1 到 9 的奇數（間隔為 2）
for i in range(1, 11, 2):   # range(1, 11, 2) 表示從 1 開始，每次 +2（所以是 1, 3, 5, 7, 9）
    print(i, end="\t")
print("\n", "--"*30)


# 第三段：印出 10 到 1 的倒數
for i in range(10, 0, -1):  # range(10, 0, -1) 表示從 10 開始，每次 -1，直到大於 0 為止
    print(i, end="\t")
print("\n", "--"*30)
